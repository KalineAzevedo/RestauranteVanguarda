// ============== FUNÇÕES DE IDIOMA ==============
function changeLanguage(lang) {
    if (!translations[lang]) return;
    
    // Salva o idioma selecionado
    localStorage.setItem("language", lang);
    
    // Atualiza a classe 'active' nos seletores de idioma
    document.querySelectorAll('.language').forEach(el => {
        el.classList.remove('active');
        if (el.id === lang) el.classList.add('active');
    });

    // Atualiza todos os elementos com IDs correspondentes
    Object.keys(translations[lang]).forEach(key => {
        const elements = document.querySelectorAll(`[id="${key}"]`);
        elements.forEach(element => {
            if (!element) return;
            
            if (element.tagName === "INPUT" || element.tagName === "TEXTAREA") {
                element.placeholder = translations[lang][key] || '';
            } else {
                element.innerHTML = translations[lang][key] || '';
            }
        });
    });

    // Casos especiais (como o rodapé)
    updateFooter(lang);
}

